void test() {
    // add max number of 3's possible
    ll n;
    cin >> n;
    ll x = 2;
    while (x * (x + 1) / 2 <= n) ++x;
    ll rem = n - (x * (x - 1)) / 2;
    // add remaining after 2 3's to form 13377....3.....7
    cout << "133" + string(rem, '7') + string(x - 2, '3') + "7" << '\n';
}

